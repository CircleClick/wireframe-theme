<p>
	It begins at a blinking terminal with a DOS line command. Vanessa Camones is 10 years old when she starts gaming in MUDs and chatting on IRC. Her father’s passion for computer engineering, and her late nights spent at that terminal, spark Vanessa’s curiosity for exploring the early Internet. Thousands of miles away, Anne Ahola Ward is among the first kids in her school to own a Commodore 64. It is just the latest gadget she enjoys tinkering with and improving, inspired by her favorite childhood movie hero -- Q, James Bond’s trusty inventor.</p>
