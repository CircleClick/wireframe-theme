<p>In 2007, Vanessa founds theMIX agency, a marketing and communications consultancy that creates content strategy and marketing programs for companies and organizations like RockYou, Geoloqi, SendGrid, Nokia, Institute for the Future, and the Stanford Center on Longevity. A year later, Anne founds CircleClick, an SEO/SEM agency that builds and enhances web engagement for companies like Heroku, Inc., oDesk, and UploadVR. </p>
<p>Their careers flourish in the succeeding years: Anne accepts a coveted invitation to IBM’s futurist program, and literally writes the book on SEO for O’Reilly Media. Vanessa authors frequent guest posts for outlets like TechCrunch and VentureBeat, speaks at conferences like the Clinton Global Initiative and SXSW (which she also advises), serves on the board of the Design Museum Foundation. They ride the seismic shifts in communication and content, led by the rise of social media and mobile, fueled further by cloud platforms and connected devices. </p>

