<p class="quote">“The common thread in my career has been growth, especially since I left Apple,” as Anne puts it now. “Traffic, sales, all the wonderful things that come with it.”</p>

<p>Vanessa begins her post-college career at Niehaus Ryan Wong, the pioneering Silicon Valley Internet and technology agency, where she works on the Pixar Animation Studios account.</p>
<!-- <h4>Lessons from a master</h4> -->
<p>“Our team got to present our marketing plan for Toy Story 2 directly to Steve Jobs -- which he literally ripped up right in front of us, then told us to start over,”  Vanessa remembers. “So I guess you could say I learned about holding myself to high expectations from the master.”</p>
<p>She also helps launch Pixar’s 3D rendering platform, explaining how its complex technologies bring strange and beautiful creatures to life.</p>

<p>This helps crystallize her love of telling great stories through and about technology, and communicating through design. It solidifies further when she helps design and launch the iRiver clix, a competitor to the iPod created with a similar standard for elegant presentation and intuitive UI. Vanessa also works at HP and Cisco before heading up marketing at Atomz, a startup acquired by WebSideStory in 2005. She spends the next decade working with dozens of companies, helping build their brand and narrative while also becoming advisor and board member at several startups.</p>
