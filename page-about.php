<p>They take their love of tech to college at a time when the Internet is first reaching the consumer market -- Anne at the University of Texas in Austin, where she works nights as an HTML editor for iBooks, an early digital content startup. Attending Santa Clara University in Silicon Valley, Vanessa is often seen lugging a brick-sized Motorola phone and a bulky laptop across campus, working as an editor for the school paper and as an intern for Lucent, Creative Labs, and Cunningham Communications -- the prestigious PR agency of record for Apple in the 90s.</p>

<p>
The story continues with Apple after college: Anne starts her career with the company’s Enterprise Education Department, helping build online influencer communities of educators. She joins the team that improves the early iTunes experience by synthesizing feedback and data directly from users. Here Anne learns that even the tiniest adjustment to a web page can lead to massive shifts in user behavior. In 2008 she brings what she’s learned at Apple to her own web development consultancy, and builds on it. While the tools for deploying sites are firmly in place, she realizes then, the tools for measuring and growing engagement on them are still evolving. Anne joins the pilot program for Google Analytics and dedicates herself to this new type of tinkering.</p>

